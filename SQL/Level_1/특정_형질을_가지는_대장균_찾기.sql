/*
### 문제: [프로그래머스] 특정 형질을 갖는 대장균 찾기 (Level 1)
### 링크: https://school.programmers.co.kr/learn/courses/30/lessons/301646

1. 문제 설명
   - 대장균들의 형질(Genotype)을 비트로 표현했을 때, 특정 조건을 만족하는 개체를 찾습니다.
   - 조건: 2번 형질이 없고(2번 비트가 0), 1번이나 3번 형질 중 하나라도 있는(1번 또는 3번 비트가 1) 개체수 출력.

2. Input (데이터 스키마)
   - ECOLI_DATA 테이블
   - 주요 컬럼: ID, GENOTYPE(정수형 형질 데이터)

3. 나의 풀이 전략
   - 비트 연산자(&)를 사용하여 정수형 데이터를 비트 단위로 필터링.
   - 2번 형질 제외: `(GENOTYPE & 2) = 0` (이진수 0010 자리 확인)
   - 1번 또는 3번 형질 포함: `(GENOTYPE & 1) > 0 OR (GENOTYPE & 4) > 0` (0001 또는 0100 자리 확인)

4. 배운 점 / 느낀 점
   - SQL에서도 비트 연산자를 통해 플래그형 데이터를 효율적으로 필터링할 수 있음을 익혔습니다.
   - 정수 데이터를 이진수로 해석하여 조건절을 구성하는 논리적 사고력을 길렀습니다.

5. 리팩토링 및 성능 최적화 (Deep Dive)
   - **효율성**: 비트 연산은 CPU 수준에서 매우 빠르게 처리되므로, 수백만 건 이상의 대용량 로그 데이터 필터링 시 매우 효율적.
   - **가독성**: `(GENOTYPE & 5) > 0`과 같이 1번(1)과 3번(4)을 합쳐서 계산할 수도 있지만, 유지보수 측면에서는 개별 조건을 `OR`로 명시하는 것이 비즈니스 로직 파악에 더 유리.
   - **확장성**: BigQuery 환경에서는 복잡한 비트 연산보다 `BIT_AND` 등의 함수를 활용하거나, 데이터 모델링 단계에서 주요 형질을 별도 컬럼으로 분리하는 것이 성능과 가독성 사이의 트레이드오프가 될 수 있음.
*/

-- 최종 쿼리
SELECT COUNT(*) AS COUNT
FROM ECOLI_DATA
WHERE (GENOTYPE & 2) = 0
  AND ((GENOTYPE & 1) > 0 OR (GENOTYPE & 4) > 0);
