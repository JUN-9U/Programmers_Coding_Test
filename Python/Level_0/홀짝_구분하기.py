# 문제: [프로그래머스] 홀짝 구분하기 (Level 0)

# 링크: https://school.programmers.co.kr/learn/courses/30/lessons/181944?language=python3

"""
### 1. 문제 설명

자연수 n이 주어진다.
n이 짝수이면 "n is even", 홀수이면 "n is odd"를 출력한다.

- 입출력 예
- 입력 #1: 100
- 출력 #1: 100 is even
- 입력 #2: 1
- 출력 #2: 1 is odd

### 2. 나의 풀이 전략

- 나머지 연산자(%)를 사용하여 정수를 2로 나눈 나머지가 0이면 짝수(even), 그렇지 않으면 홀수(odd)로 판단하는 if-else 조건문을 설계.
- f-string 포맷팅을 활용하여 입력받은 변수 a를 문자열과 직관적으로 출력.

### 3. 배운 점 / 느낀 점

- 비즈니스 로직이나 데이터 전처리 과정에서 특정 조건에 따라 데이터를 분류하는 기초적인 연산인 조건 분기(if-else)의 흐름을 복습했습니다.
- 문자열과 변수를 복잡하게 더하는 방식 대신 f-string을 활용함으로써 코드의 가독성을 높이고 문법적 오류를 방지하는 효율적인 출력 방식을 체감했습니다.

### 4. 리팩토링 및 성능 최적화 (Deep Dive)

- **비트 연산:** 컴퓨터는 모든 데이터를 이진수로 처리하므로, 정수의 최하위 비트(LBS)가 0이면 짝수, 1이면 홀수가 된다. 따라서 대용량 산술 연산 환경에서는 비용이 상대적으로 적은 비트 AND 연산(a & 1)을 활용하는 것이 최적화 기법이 될 수 있다.
- **삼향 연산자와 코드 응축:** 단순한 이진 분기 `if-else` 구조에서는 파이썬의 조건부 표현식을 활용해 `print(f"{a} is {'even' if a % 2 == 0 else 'odd'}")와 같이 한 줄로 축약 가능.
"""

# 최종 풀이 코드

a = int(input())

if a % 2 == 0:
    print(f"{a} is even")
else:
    print(f"{a} is odd")
