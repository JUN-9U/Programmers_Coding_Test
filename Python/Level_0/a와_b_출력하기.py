# 문제: [프로그래머스] a와 b 출력하기 (Level 0)
# 링크: https://school.programmers.co.kr/learn/courses/30/lessons/181951

"""
### 1. 문제 설명
정수 a와 b가 공백으로 구분되어 주어집니다.
입력받은 a와 b의 값을 "a = 값", "b = 값" 형식으로 출력합니다.

- 입출력 예
- 입력 #1
  4 5
- 출력 #1
  a = 4
  b = 5

### 2. 나의 풀이 전략
- `input().strip().split(' ')`을 통해 입력 문자열의 양끝 공백을 제거하고 공백 기준으로 분리.
- `map(int, ...)`을 활용하여 분리된 문자열 리스트를 정수형으로 일괄 변환하여 변수 a, b에 할당.
- **f-string** 방식을 사용하여 변수와 문자열을 직관적으로 조합.

### 3. 배운 점 / 느낀 점
- `map()` 함수를 사용하면 반복문 없이도 여러 데이터를 한 번에 특정 자료형으로 변환할 수 있어 코드가 간결해집니다.
- `print(f"a = {a}")`와 같은 f-string 방식이 기존의 `%` 연산자나 `.format()` 방식보다 가독성이 훨씬 뛰어나다는 점을 익혔습니다.

### 4. 리팩토링 및 성능 최적화 (Deep Dive)
- **가독성:** 데이터 분석 리포트를 자동화하거나 로그를 남길 때 f-string을 사용하면 변수명을 직관적으로 확인할 수 있어 유지보수에 유리.
- **데이터 타입:** 입력받은 값을 `int`로 변환하는 과정은 이후의 산술 연산을 위해 필수적인 단계이며, 대용량 데이터를 다룰 때 각 컬럼의 데이터 타입을 정의하는 것과 같은 맥락.
"""

# 최종 풀이 코드
a, b = map(int, input().strip().split(' '))
print(f"a = {a}")
print(f"b = {b}")
