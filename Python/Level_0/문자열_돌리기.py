# 문제: [프로그래머스] a와 b 출력하기 (Level 0)

# 링크: https://school.programmers.co.kr/learn/courses/30/lessons/181945?language=python3

"""
### 1. 문제 설명

문자열 str이 주어집니다.
문자열을 시계방향으로 90도 돌려서 출력하는 코드를 작성하세요.

- 입출력 예
- 입력 #1: abcde
- 출력 #1: a
           b
           c
           d
           e

### 2. 나의 풀이 전략

- 파이썬의 문자열은 순회 가능한 객체임을 인지.
- for 반복문을 사용하여 각 문자에 순차적으로 접근.
- print() 함수는 호출될 때마다 자동으로 줄바꿈을 수행하기 때문에 추가적인 제어 없이도 문자열을 세로로 나열.

### 3. 배운 점 / 느낀 점

- 파이썬의 문자열은 단순한 텍스트가 아닌, 각 문자가 순서대로 나열된 '시퀀스 데이터'라는 점을 이해하고, 자유롭게 반복문(for)을 활용할 수 있다는 점을 배웠습니다.
- print() 함수의 기본 원리가 자동으로 줄바꿈을 수행한다는 특성을 활용해 출력 형식을 제어하는 기초를 다졌습니다.

### 4. 리팩토링 및 성능 최적화 (Deep Dive)

- **성능 최적화:** 반복문 내에서 `print()`를 매번 호출하는 방식보다, `'\n.join(str)`을 사용하여 문자열을 한 번에 결합해 출력하는 것이 입출력 호출 횟수를 줄여 대용량 데이터 처리 시 훨씬 효율적.
- **데이터 구조화:** 단일 가로 데이터를 세로 형태의 행(Row) 단위로 전개하는 과정은 비정형 데이터를 구조화된 데이터셋으로 변환하는 '데이터 정규화'의 가장 기초적인 단계.
"""

# 최종 풀이 코드

str = input()

for i in str:
    print(i)
